class VkBot:
    """–ö–ª–∞—Å—Å –±–æ—Ç–∞. –°–æ–¥–µ—Ä–∂–∏—Ç —Å–±–æ—Ä id –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è, –µ–≥–æ –∏–º–µ–Ω–∏, –∞ —Ç–∞–∫–∂–µ –ø—Ä–æ—Å—Ç–µ–π—à–∏–µ –∫–æ–º–∞–Ω–¥—ã —Å–∞–º–æ–≥–æ –±–æ—Ç–∞, —Ç—Ä–µ–±—É—é—â–∏–µ—Å—è
    –¥–ª—è –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏—è –∏ –ø—Ä–æ—â–∞–Ω–∏—è."""

    # __init__ - –ø—É–±–ª–∏—á–Ω—ã–π –º–µ—Ç–æ–¥; self - —Å—Å—ã–ª–∞–µ—Ç—Å—è –Ω–∞ —Å–∞–º –æ–±—ä–µ–∫—Ç
    def __init__(self, user_id):
        # –ó–¥–µ—Å—å —Ö—Ä–∞–Ω–∏—Ç—Å—è —é–∑–µ—Ä id
        self._USER_ID = user_id
        # –ó–¥–µ—Å—å —Ö—Ä–∞–Ω–∏—Ç—Å—è –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –¥–ª—è –æ–±—Ä–∞—â–µ–Ω–∏—è –≤ —Å–æ–æ–±—â–µ–Ω–∏—è—Ö
        self._USERNAME = self._get_user_name_from_vk_id(user_id)
        # –ü—Ä–æ—Å—Ç–µ–π—à–∏–µ –ø–µ—Ä–≤–∏—á–Ω—ã–µ –∫–æ–º–∞–Ω–¥—ã
        self._COMMANDS = ["–ø—Ä–∏–≤–µ—Ç",
                          "ghbdtn",
                          "–ø–æ–∫–∞"]

    def _get_user_name_from_vk_id(self, user_id):
        """–ú–µ—Ç–æ–¥ –ø–æ–ª—É—á–∞–µ—Ç –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–µ—Ä–µ–∑ –µ–≥–æ id"""
        # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º id —é–∑–µ—Ä–∞
        request = requests.get("https://vk.com/id" + str(user_id))
        # –ø–∞—Ä—Å–∏–º
        soup = bs4.BeautifulSoup(request.text, "html.parser")
        user_name = self._clean_all_tag_from_str(soup.findAll("title")[0])
        # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        return user_name.split()[0]

    def read_command_file(self):
        """–ú–µ—Ç–æ–¥ —á–∏—Ç–∞–µ—Ç —Ñ–∞–π–ª —Å –∫–æ–º–∞–Ω–¥–∞–º–∏ –±–æ—Ç–∞ –∏ –≤–æ–≤—Ä–∞—â–∞–µ—Ç –∏—Ö"""
        # –ß–∏—Ç–∞–µ–º —Ñ–∞–π–ª, –≤ –∫–æ—Ç–æ—Ä–æ–º –Ω–∞—Ö–æ–¥—è—Ç—Å—è –≤—Å–µ –∫–æ–º–∞–Ω–¥—ã –±–æ—Ç–∞
        commandsFile = open("commands.txt")
        return commandsFile.read()

    def new_message(self, message):
        """–ú–µ—Ç–æ–¥ –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –æ—Ç–≤–µ—Ç"""
        # –ü—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ. –¢–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –æ–ø—É—Å–∫–∞–µ–º –≤ –Ω–∏–∂–Ω–∏–π —Ä–µ–≥–∏—Å—Ç—Ä, —á—Ç–æ–±—ã –Ω–µ –±—ã—Ç—å –∫ –Ω–µ–º—É —á—É–≤—Å—Ç–≤–∏—Ç–µ–ª—å–Ω—ã–º–∏
        if message.lower() == self._COMMANDS[0]:
            return f"–ü—Ä–∏–≤–µ—Ç, {self._USERNAME}! –Ø - –ó–æ–¥–∏–∞–∫—É—Å - —Ç–≤–æ–π –ø—Ä–æ–≤–æ–¥–Ω–∏–∫ –≤ –º–∏—Ä —Ç–æ–ª–∫–æ–≤–∞–Ω–∏—è –∑–≤—ë–∑–¥.\n " \
                   f"–í–æ—Ç —Å–ø–∏—Å–æ–∫ –º–æ–∏—Ö –∫–æ–º–∞–Ω–¥:\n" + self.read_command_file()
        # –†–∞—Å–∫–ª–∞–¥–∫–∞ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
        elif message.lower() == self._COMMANDS[1]:
            return f"–î–ª—è –æ–±—â–µ–Ω–∏—è –ø–æ–º–µ–Ω—è–π —Ä–∞—Å–∫–ª–∞–¥–∫—É –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã - —è –ø–æ–Ω–∏–º–∞—é —Ç–æ–ª—å–∫–æ —Ä—É—Å—Å–∫–∏–π üòú"
        # –ü—Ä–æ—â–∞–Ω–∏–µ
        elif message.lower() == self._COMMANDS[2]:
            return f"–ü–æ–∫–∞-–ø–æ–∫–∞, {self._USERNAME}!"
        else:
            return f"–Ø –Ω–∏—á–µ–≥–æ –Ω–µ –ø–æ–Ω—è–ª... –ü–æ–º–Ω–∏, —á—Ç–æ –º–æ–∏ –∫–æ–º–∞–Ω–¥—ã –≤–≤–æ–¥—è—Ç—Å—è —á–µ—Ä–µ–∑ /. " \
                   f"–í–≤–µ–¥–∏ /–∫–æ–º–∞–Ω–¥—ã, —á—Ç–æ–±—ã –ø–æ—Å–º–æ—Ç—Ä–µ—Ç—å –∏—Ö —Å–ø–∏—Å–æ–∫."

    # –ú–µ—Ç–æ–¥ –¥–ª—è –æ—á–∏—Å—Ç–∫–∏ –æ—Ç –Ω–µ–Ω—É–∂–Ω—ã—Ö —Ç—ç–≥–æ–≤
    @staticmethod
    def _clean_all_tag_from_str(string_line):
        """
        –û—á–∏—Å—Ç–∫–∞ —Å—Ç—Ä–æ–∫–∏ stringLine –æ—Ç —Ç—ç–≥–æ–≤ –∏ –∏—Ö —Å–æ–¥–µ—Ä–∂–∏–º—ã—Ö
        :param string_line: –û—á–∏—â–∞–µ–º–∞—è —Å—Ç—Ä–æ–∫–∞
        :return: –æ—á–∏—â–µ–Ω–Ω–∞—è —Å—Ç—Ä–æ–∫–∞
        """
        result = ""
        not_skip = True
        for i in list(string_line):
            if not_skip:
                if i == "<":
                    not_skip = False
                else:
                    result += i
            else:
                if i == ">":
                    not_skip = True

        return result
